# 3. Over the summer, temperatures in Yorkshire reached 38.4C. Write a program that
# converts this value in Celsius to the equivalent temperature in Fahrenheit, and then displays both.


# Program to convert and display temperature from Celsius to Fahrenheit
summer_temperature_celsius = 38.4

# Converting Celsius to Fahrenheit
summer_temperature_fahrenheit = (9/5) * summer_temperature_celsius + 32

# Displaying results
print(f"Summer Temperature in Celsius: {summer_temperature_celsius}C")
print(f"Summer Temperature in Fahrenheit: {summer_temperature_fahrenheit}F")

